    public void loadUserList() {
        String type = cboType.getSelectedItem().toString();
        String strSQL = "";
        
        // 선택된 타입에 따라 쿼리 변경
        if (type.equals("학생")) {
            strSQL = "SELECT stu_id, name, dept, password FROM Student";
        } else {
            strSQL = "SELECT prof_id, name, dept, password FROM Professor";
        }

        try {
            DBM.dbOpen();
            DBM.DB_rs = DBM.DB_stmt.executeQuery(strSQL);
            
            DefaultTableModel model = (DefaultTableModel) tblUsers.getModel();
            model.setRowCount(0);
            
            while(DBM.DB_rs.next()) {
                Vector v = new Vector();
                // 첫 번째 컬럼(ID)이 학생이면 stu_id, 교수면 prof_id
                v.add(DBM.DB_rs.getString(1)); 
                v.add(DBM.DB_rs.getString("name"));
                v.add(DBM.DB_rs.getString("dept"));
                v.add(DBM.DB_rs.getString("password"));
                model.addRow(v);
            }
            DBM.dbClose();
        } catch (Exception e) {
            System.out.println("목록 로드 에러: " + e.getMessage());
        }
    }
