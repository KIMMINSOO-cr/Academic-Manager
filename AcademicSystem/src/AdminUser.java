    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblUsers.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "삭제할 사용자를 선택하세요.");
            return;
        }
        
        // 선택된 ID 가져오기
        String selectedID = tblUsers.getValueAt(selectedRow, 0).toString();
        String type = cboType.getSelectedItem().toString();
        
        int confirm = JOptionPane.showConfirmDialog(this, 
                "선택한 사용자(" + selectedID + ")를 정말 삭제하시겠습니까?\n관련된 모든 데이터(성적, 강의 등)가 함께 삭제됩니다.", 
                "삭제 확인", JOptionPane.YES_NO_OPTION);
        
        if (confirm != JOptionPane.YES_OPTION) return;

        String strSQL = "";
        if (type.equals("학생")) {
            strSQL = "DELETE FROM Student WHERE stu_id = '" + selectedID + "'";
        } else {
            strSQL = "DELETE FROM Professor WHERE prof_id = '" + selectedID + "'";
        }

        try {
            DBM.dbOpen();
            DBM.DB_stmt.executeUpdate(strSQL);
            DBM.dbClose();
            
            JOptionPane.showMessageDialog(this, "삭제되었습니다.");
            loadUserList(); // 목록 갱신
            
        } catch (Exception e) {
            System.out.println("삭제 에러: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "삭제 실패: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnDeleteActionPerformed


