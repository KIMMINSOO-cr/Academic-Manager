    public void loadCourseList() {
        try {
            String strSQL = "SELECT lecture_id, title FROM Lecture WHERE prof_id = '" + myID + "'";
            DBM.dbOpen();
            DBM.DB_rs = DBM.DB_stmt.executeQuery(strSQL);
            
            cboLecture.removeAllItems(); // 기존 목록 비우기
            while (DBM.DB_rs.next()) {
                // 콤보박스에 "강의명(강의코드)" 형태로 넣기
                String item = DBM.DB_rs.getString("title") + "(" + DBM.DB_rs.getString("lecture_id") + ")";
                cboLecture.addItem(item);
            }
            DBM.dbClose();
        } catch (Exception e) {
            System.out.println("강의 목록 로드 에러: " + e.getMessage());
        }
    }
