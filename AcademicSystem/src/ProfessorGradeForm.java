    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String selectedItem = cboLecture.getSelectedItem().toString();
        // "자바(CS101)" 에서 괄호 안의 "CS101"만 추출하기
        String lectureID = selectedItem.substring(selectedItem.indexOf("(") + 1, selectedItem.indexOf(")"));

        try {
            // 수강신청(E) 테이블과 학생(S) 테이블 조인
            String strSQL = "SELECT E.enroll_id, S.stu_id, S.name, E.score, E.grade "
                          + "FROM Enrollment E "
                          + "JOIN Student S ON E.stu_id = S.stu_id "
                          + "WHERE E.lecture_id = '" + lectureID + "'";
            
            DBM.dbOpen();
            DBM.DB_rs = DBM.DB_stmt.executeQuery(strSQL);
            
            DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
            model.setRowCount(0);
            
            while(DBM.DB_rs.next()) {
                Vector v = new Vector();
                v.add(DBM.DB_rs.getInt("enroll_id")); // 수정 기준 키 (숨겨도 됨)
                v.add(DBM.DB_rs.getString("stu_id"));
                v.add(DBM.DB_rs.getString("name"));
                v.add(DBM.DB_rs.getInt("score"));
                v.add(DBM.DB_rs.getString("grade"));
                model.addRow(v);
            }
            DBM.dbClose();
        } catch (Exception e) {
            System.out.println("수강생 조회 에러: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        int selectedRow = tblStudents.getSelectedRow();
        // 선택한 줄의 데이터 가져오기
        selectedEnrollID = tblStudents.getValueAt(selectedRow, 0).toString(); // 고유번호 저장
        txtScore.setText(tblStudents.getValueAt(selectedRow, 3).toString());
        
        // 등급이 null일 수 있으므로 체크
        Object gradeObj = tblStudents.getValueAt(selectedRow, 4);
        if(gradeObj != null) txtGrade.setText(gradeObj.toString());
        else txtGrade.setText("");
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedEnrollID == null) {
            JOptionPane.showMessageDialog(this, "학생을 선택해주세요.");
            return;
        }
        
        String score = txtScore.getText();
        String grade = txtGrade.getText();
        
        // 점수 업데이트 쿼리
        String strSQL = "UPDATE Enrollment SET score = " + score + ", grade = '" + grade + "' "
                      + "WHERE enroll_id = " + selectedEnrollID;
        
        try {
            DBM.dbOpen();
            DBM.DB_stmt.executeUpdate(strSQL);
            DBM.dbClose();
            
            JOptionPane.showMessageDialog(this, "성적 입력 완료!");
            btnSearch.doClick(); // 목록 새로고침 (자동 클릭 효과)
            
        } catch (Exception e) {
            System.out.println("업데이트 에러: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


